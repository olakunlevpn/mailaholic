# embed-assets.ps1
# Converts WebAssets folder to C++ header with gzipped byte arrays

param(
    [string]$InputDir = "hmailserver/source/WebAssets",
    [string]$OutputFile = "hmailserver/source/Server/Common/WebAdmin/EmbeddedAssets.h"
)

$assets = @{}

function Compress-ToGzip {
    param([byte[]]$Data)
    $ms = New-Object System.IO.MemoryStream
    $gz = New-Object System.IO.Compression.GZipStream($ms, [System.IO.Compression.CompressionMode]::Compress)
    $gz.Write($Data, 0, $Data.Length)
    $gz.Close()
    return $ms.ToArray()
}

function Convert-ToHexArray {
    param([byte[]]$Data)
    $hex = ($Data | ForEach-Object { "0x{0:X2}" -f $_ }) -join ", "
    return $hex
}

# Resolve paths
$scriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path
$repoRoot = Split-Path -Parent $scriptDir
$InputDir = Join-Path $repoRoot $InputDir
$OutputFile = Join-Path $repoRoot $OutputFile

Write-Host "Input directory: $InputDir"
Write-Host "Output file: $OutputFile"

# Ensure output directory exists
$outputDir = Split-Path -Parent $OutputFile
if (-not (Test-Path $outputDir)) {
    New-Item -ItemType Directory -Force -Path $outputDir | Out-Null
}

# Collect all files
$assetPaths = @()
Get-ChildItem -Path $InputDir -Recurse -File | ForEach-Object {
    $relativePath = $_.FullName.Substring((Resolve-Path $InputDir).Path.Length + 1).Replace("\", "/")
    $content = [System.IO.File]::ReadAllBytes($_.FullName)
    $compressed = Compress-ToGzip -Data $content
    $assets[$relativePath] = @{
        Data = $compressed
        Size = $compressed.Length
        OriginalSize = $content.Length
    }
    $assetPaths += $relativePath
    Write-Host "Embedded: $relativePath ($($content.Length) -> $($compressed.Length) bytes)"
}

# Sort paths for consistent ordering
$assetPaths = $assetPaths | Sort-Object

# Generate C++ header
$header = @"
// EmbeddedAssets.h - Auto-generated by embed-assets.ps1, do not edit
#pragma once

#include <string>
#include <vector>
#include <unordered_map>

namespace WebAdmin
{
   struct Asset
   {
      const unsigned char* data;
      size_t compressedSize;
      size_t originalSize;
      const char* mimeType;
   };


"@

# Add byte arrays
$assetIndex = 0
foreach ($path in $assetPaths) {
    $asset = $assets[$path]
    $varName = "asset_data_$assetIndex"
    $hexData = Convert-ToHexArray -Data $asset.Data
    $header += "   static const unsigned char ${varName}[] = { $hexData };`r`n"
    $assetIndex++
}

# Add asset map
$header += @"

   inline const std::unordered_map<std::string, Asset>& GetAssetMap()
   {
      static std::unordered_map<std::string, Asset> assets = {
"@

$assetIndex = 0
foreach ($path in $assetPaths) {
    $asset = $assets[$path]
    $varName = "asset_data_$assetIndex"

    # Determine MIME type
    $mimeType = "application/octet-stream"
    if ($path.EndsWith(".html")) { $mimeType = "text/html" }
    elseif ($path.EndsWith(".css")) { $mimeType = "text/css" }
    elseif ($path.EndsWith(".js")) { $mimeType = "application/javascript" }
    elseif ($path.EndsWith(".json")) { $mimeType = "application/json" }
    elseif ($path.EndsWith(".png")) { $mimeType = "image/png" }
    elseif ($path.EndsWith(".svg")) { $mimeType = "image/svg+xml" }
    elseif ($path.EndsWith(".ico")) { $mimeType = "image/x-icon" }

    $header += "         {`"$path`", {$varName, $($asset.Size), $($asset.OriginalSize), `"$mimeType`"}},`r`n"
    $assetIndex++
}

$header += @"
      };
      return assets;
   }

   inline bool GetAsset(const std::string& path, const unsigned char*& data, size_t& compressedSize, size_t& originalSize, const char*& mimeType)
   {
      auto& assets = GetAssetMap();
      auto it = assets.find(path);
      if (it == assets.end())
         return false;

      data = it->second.data;
      compressedSize = it->second.compressedSize;
      originalSize = it->second.originalSize;
      mimeType = it->second.mimeType;
      return true;
   }
}
"@

# Write output
$header | Out-File -FilePath $OutputFile -Encoding UTF8
Write-Host "Generated: $OutputFile"
